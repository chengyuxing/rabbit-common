package com.github.chengyuxing.common.script.lang;

/**
 * For loop context property enums.
 */
public enum ForContextProperty {
    /**
     * Index of the current item
     */
    index,
    /**
     * Whether the current item is the first item
     */
    first,
    /**
     * Whether the current item is the last item
     */
    last,
    /**
     * Whether the current item index is even
     */
    odd,
    /**
     * Whether the current item index is odd
     */
    even
}
